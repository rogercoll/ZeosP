void system_call_handler();

void setIdt()
{
  /* Program interrups/exception service routines */
  idtR.base  = (DWord)idt;
  idtR.limit = IDT_ENTRIES * sizeof(Gate) - 1;

  set_handlers();

  setInterruptHandler(33, keyboard_handler, 0);  //aqui s'inicialitza la IDT
  setInterruptHandler(32, clock_handler, 0);  //aqui s'inicialitza la IDT
  setTrapHandler(0x80, system_call_handler, 3);

  set_idt_reg(&idtR);
}

